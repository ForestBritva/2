#include <iostream>
#include <vector>
#include <cctype> // Для ::tolower
using namespace std;

int main() {
    vector<string> *строки = new vector<string>(); // Создаём вектор строк
    строки->push_back("AbC"); // Добавляем строку "AbC"
    строки->push_back("dEfh"); // Добавляем строку "dEfh"
    
    // Выводим только гласные буквы из каждой строки
    for(string строка : *строки) {
        for(char символ : строка) {
            char нижний_символ = ::tolower(символ); // Приводим к нижнему регистру для проверки
            if(нижний_символ == 'a' || нижний_символ == 'e' || нижний_символ == 'i' ||
               нижний_символ == 'o' || нижний_символ == 'u') {
                cout << символ; // Выводим оригинальный символ (сохраняя регистр)
            }
        }
        cout << endl; // Перенос строки после обработки каждой строки
    }
    
    delete строки; // Освобождаем память
    return 0;
}